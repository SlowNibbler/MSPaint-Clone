package view;

import java.awt.Color;
import java.awt.Shape;
import java.util.ArrayList;
import java.util.List;

/**
 * 
 * @author jamesm47
 * @version 11/23/18
 */
public final class PaintShapes {
    
    /***Initializes an array list to store shapes to be painted.*/
    protected static List<Shape> shapeArray = new ArrayList<Shape>();
    /***Initializes an array list to store colors of shapes to be painted.*/
    protected static List<Color> colorArray = new ArrayList<Color>();
    /***Initializes an array list to store thickness values of shapes to be painted.*/
    protected static List<Integer> thicknessArray = new ArrayList<Integer>();
    
    /**
     * default empty constructor for the PaintShapes class.
     */
    private PaintShapes() {
        
    }
    
    /**
     * adds a shape to the shape array.
     * @param theNewShape the added shape.
     */
    public static void addShape(final Shape theNewShape) {
        shapeArray.add(theNewShape);
    }
    /**
     * adds a color of a shape to the color array.
     * @param theNewColor the added color.
     */
    public static void addColor(final Color theNewColor) {
        colorArray.add(theNewColor);
    }
    /**
     * adds a thickness value of a shape to the thickness array.
     * @param theNewThick the added thickness.
     */
    public static void addThickness(final int theNewThick) {
        thicknessArray.add(theNewThick);
    }
    
    /**
     * returns the shape array.
     * @return the shape array.
     */
    public static List<Shape> getShapeArray() {
        return shapeArray;
    }
    
    /**
     * returns the color array.
     * @return the color array.
     */
    public static List<Color> getColorArray() {
        return colorArray;
    }
    
    /**
     * returns the thickness array.
     * @return the thick array.
     */
    public static List<Integer> getThicknessArray() {
        return thicknessArray;
    }
    /**
     * clears all of the arrays.
     */
    public static void clearLists() {
        shapeArray.clear();
        colorArray.clear();
        thicknessArray.clear();
        
    }
    
}
